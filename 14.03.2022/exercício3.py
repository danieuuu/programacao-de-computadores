print('1 ano = 365 dias, 1 mês = 30 dias.')
anos = int(input('Digite a sua idade somente em anos: '))
meses = int(input('Digite a sua idade somente em meses: '))
dias = int(input('Digite a sua idade somente em dias: '))
idade = anos * 365 + (meses * 30) + dias

print(f'Você tem {idade} dias de vida')

